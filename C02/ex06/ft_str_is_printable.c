#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	j = 1;
	while (str[i] != '\0')
	{
		c = str[i];
		if (!((c > 31 && c < 127) || (str[0] == '\0')))
		{
			j = 0;
		}
		i++;
	}
	return (j);
}
